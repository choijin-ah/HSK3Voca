// 검증: 1..600 전부 정확히 한 번씩
const seen = {};
const dups = [];
GROUPS.forEach(function (g) {
  g.nums.forEach(function (n) {
    if (seen[n]) dups.push(n);
    seen[n] = true;
    if (!byNum[n]) throw new Error('존재하지 않는 번호: ' + n);
  });
});
const missing = Object.keys(byNum).map(Number).filter(function (n) { return !seen[n]; });
if (dups.length) throw new Error('중복 번호: ' + dups.join(', '));
if (missing.length) throw new Error('누락 번호: ' + missing.sort(function (a, b) { return a - b; }).join(', '));

// 새 categories 구성 — 단어 객체는 원본 그대로 재사용
const pad = function (n) { return n < 10 ? '0' + n : '' + n; };
const categories = GROUPS.map(function (g, i) {
  return {
    id: 'g' + pad(i + 1),
    title: g.title,
    count: g.nums.length,
    priority: g.badge,
    description: g.desc,
    words: g.nums.map(function (n) { return byNum[n]; }),
  };
});

const newSet = {
  id: set.id,
  title: set.title,
  pageTitle: set.pageTitle,
  subtitle: '단어를 사전순이 아니라 “비슷한 뜻” 기준으로 묶었습니다. 총 600개 단어.',
  language: set.language,
  labels: set.labels,
  searchPlaceholder: set.searchPlaceholder,
  source: set.source,
  wordCount: set.wordCount,
  categories: categories,
};

